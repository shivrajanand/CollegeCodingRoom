/*Task : To demonstrate working of arithmetic operators*/

#include <iostream>

using namespace std;

int main()
{
    int a, b;

    cout << "Enter value of first number: ";
    cin >> a;
    cout << "Enter value of second number: ";
    cin >> b;

    cout << "a + b = " << (a + b) << "\n";
    cout << "a - b = " << (a - b) << "\n";
    cout << "a x b = " << (a * b) << "\n";
    cout << "a / b = " << (a / b) << "\n";
    cout << "a % b = " << (a % b) << "\n";
    return 0;
}